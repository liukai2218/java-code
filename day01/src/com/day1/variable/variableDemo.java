package com.day1.variable;

public class variableDemo {
    public static void main(String[] args) {
        variableDemo();
    }

    public static void variableDemo(){
        int age = 18; //定义一个整型变量age并赋值为18
        System.out.println("age = " + age); //输出变量age的值
        //定义一个小数变量height并赋值为1.75
        double height = 1.75;
        System.out.println("height = " + height); //输出变量height的值
        //定义一个字符变量grade并赋值为'A'
        char grade = 'A';
        System.out.println("grade = " + grade); //输出变量grade的值

    }
}
