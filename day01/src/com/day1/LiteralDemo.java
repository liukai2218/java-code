package com.day1;
//掌握常见字面量
public class LiteralDemo {
    public static void main(String[] args) {
        printLieral();
    }
    public static void printLieral(){
        //帮我输出常见自变量
        System.out.println("liukai2025\n /11/24");
        System.out.println(100); //整数型字面量
        System.out.println(3.14); //浮点型字面量
        System.out.println('A'); //字符型字面量
        System.out.println("Hello,World!"); //字符串型字面量
        System.out.println(true); //布尔型字面量

    }
}
